                <h1>Site Plan for Salmon River Adventures</h1>

                <h2>Purpose</h2>
                <p>The purpose of this site is to introduce the Salmon River Adventures Company and to advertise positively it's services to the public. It will contain pages to help answer questions as to the different activities the company does. It will also introduce individuals the company has hired on as guides for these different activities. A visit to the site should give visitors a excited desire to visit the salmon river and to trust the company with their safety. 
 
                </p>
                <h2>Target Audience</h2>
                <p>The target audience will be young adults ages 18-35. These people will be of various skill levels. Some seasoned rafters and some first timers. Most of them will be very enthusiastic about rafting and be looking for ways to make life more exciting. These people are not necessarily computer people so the site will be fairly easy to manage. Laptops and smart phones will be the typical devices used to access the site. The site will most likely be accessed for about 20 min to look at the information and contact the office through the contact page. It will mostly be accessed by people local to the north western states but occasionally we will get people form out of the area coming to raft the river. 
 
                </p>
