                <h2>MUSCLES LOOK GOOD </h2>



                <p>It’s true, if you dieted enough and burned enough calories, you’d get thin.  Maybe even as thin as you wanted.  But who wants a little girl with skeleton arms?  In short, muscles give you curves and prevents the loss of lean body mass as well as fat.</p>



                <h2>BUT WON’T I GET BULKY?</h2>



                <p>No no no!  Biggest myth ever!  Many girls don’t lift simply because they don’t want to look like Arnold Schwartzenegger (understandable), but what they don’t understand is that unless they intend on taking anabolic STEROIDS, they’re not gonna look like Mr. Universe.  Women’s testosterone levels are lower than men’s, and without sufficient hormone levels, your muscles won’t bulk the way a man’s do.  Instead of looking like Conan the Barbarian, you’ll look a little more like Xena Warrior Princess (and who wouldn’t want that??)</p>



                <h2>MUSCLES BURN FAT</h2>



                <p>If it’s fat loss you’re going for, most of us are, then building muscle is the supreme way to work smart, instead of hard.  Your body weight goes purely off of Calories in – Calories out, so naturally, if you burn more calories than you take in, you’ll lose weight.  HOW you burn those calories is up to you.  Would you rather burn those calories spinning and spinning and spinning on that cycle at the gym or at home watching your favorite show?  Now I’m not saying that you can burn calories while chilling out eating potato chips, but lean muscle burns more fat while you’re NOT working out, enabling you to burn more calories with less effort.  Gaining lean muscle mass increases your metabolism and causes you to burn more calories while not working out- therein burning more calories and causing more fat loss.</p>



                <p>So think about it- Do you wanna work hard?  Or work smart?

