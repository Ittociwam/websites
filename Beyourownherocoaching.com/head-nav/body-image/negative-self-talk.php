                <h1>Changing Your Negative Self Talk</h1>
                <img src="/media/image/negative-self-talk.jpg" alt="Negative Self Talk">
                <p>



                    If I stood you in front of a mirror and asked you what you thought about the girl in the mirror, what would you say?  I hope that you would say that you see a beautiful girl with an incredible potential- that’s what I would see- but let’s be honest, this isn’t always how we feel about ourselves.  I talk to myself all the time (ask my husband) both out loud and in my head, and when I actually listen to what I’m saying, those things that I tell myself in my head are often extremely hurtful and damaging.  Self talk almost goes unnoticed, especially since most of the time we are the only ones that hear it, but it can be either extremely helpful or harmful as we try to achieve our goals and become the person we’re trying to be.  I can’t tell you how many times I’ve looked into a mirror and critiqued and criticized every little “flaw” that I see, and the backlash that I feel after that is overwhelming.  No matter what your size, shape, hair color, body structure, ANYTHING- You need to know how beautiful you are, and you need to make yourself believe it.  Every hurtful thing we tell ourselves undermines all of our hard work in trying to become better and can cut us right to the core.  So how to we make a change?  Before we can make a change and begin to see ourselves in a better light, we need to realize all of the negative self talk that is happening.  When I decided to really listen to how often I was criticizing myself, I was alarmed at what I was telling myself and especially how OFTEN it was happening.   I would never say these things to other people, so why was I saying it to myself??

                </p>
                <p>
                    <b>THE CHALLENGE: </b> Grab a journal, notebook, scratch piece of paper, anything- and keep it with you ALL the time.  Your first assignment is to start listening.  When you say anything to yourself about your appearance of your body, listen first to what you’re saying, and then write it down.  And I don’t want you to sugar coat it when you write it down- write down your exact thought.  Do these for at least a day, preferably 2-3 days.  After you’ve done this for a while, I want you to sit down and reread over your list.  Read the things that you’re saying to yourself, and ask yourself if those things are REALLY true.  Would you ever say those things to anyone other than yourself?  No?  Then WHY are you saying them to you?  Now that you’re really listening, I want you to make a habit.  Every time you hear a negative thought, you come right back and combat it with something opposite.   AKA “Ugh, really hating my love handles in this shirt… But these pants make my butt look BANGIN!”   “Man, she’s so skinny- I wish I was her…  But I’d really  miss my beautiful long brown hair!”  Whatever the case may be.  Downplay what you feel are your “flaws” and highlight your assets!  As you begin to get into the habit, of combating your own sabotaging, you’ll realize how beautiful you really are and start actually believing it!</p>

