                <h2>Navigation</h2>
                <p>The navigation for my pages will consist of a navigation bar with drop down menus that can either lead 
                    to individual links under that category or to a secondary that describes the function of that category. It 
                    will also consist of a footer that contains links to pages such as site map and contact us. I want the nav 
                    bar at the top of the page to remain stationary in the same spot even when scrolling like on the Netflix 
                    website. I want each menu item on the nav bar to light up or animate when hovered over and I would 
                    like each one to stay lit up when the user is on that specific page. I would also like the logo to always link 
                    back to the first page. The home page will have both the Nav bar at the top and also pictures that 
                    represent each link and a little about each one so that if the user is not familiar with the site he/she can 
                    read over the pictures and text and determine what link to click on. I like the example of this on 
                    ubnt.com there is a rotating picture menu below the nav bar that has links to several different pages so 
                    that the user can get familiar with the site without diving deep into it. 
                </p>

                <h2>Responsiveness</h2>
                <p>Navigation will condense as screen gets smaller. The Options to watch the introductory or go straight to 
                    the coaching page will appear at the bottom as tabs in the cell phone screen, giving even the small 
                    screens the same functionality.</p>
