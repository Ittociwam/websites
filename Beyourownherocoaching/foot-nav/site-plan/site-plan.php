                <h1>Site Plan for Be Your Own Hero Coaching</h1>

                <h2>Purpose</h2>
                <p>The purpose of the site will be to attract positive attention from people who want help achieving health 
                    and fitness goals. The objective is to help these people change to a healthier lifestyle and obtain a 
                    positive train of thought. The site will have courses on video and through podcast that give instruction 
                    and motivation to these people. It will also contain articles and blog posts on diet and exercise. It will 
                    have tools such as a calendar for tracking progress and a way to sign up for one on one individual help in 
                    the local Rexburg area. 
                </p>
                <h2>Target Audience</h2>
                <p>Our target audience will consist of mostly females between 16 and 35 years old of all economic statuses. 
                    Most will be attending college but site will also aim towards late high-school students. Most likely our 
                    users will have already held jobs. They are accessing the site out of a desire to become more healthy 
                    and/or lose weight. They want to boost self-confidence and gain a better body image. Our target 
                    audience spends a lot of time on blogs and social networking sites. They will access the site in spare 
                    time, while or just before working out, and possibly when deciding what plan for meals. Laptops and 
                    smart phones are primary web devices. (emphasis on the smartphone or iPod because our users will 
                    most like be accessing the site while exercising). Our users will most likely use the site for 15 to 30 min a 
                    day. Personal training services from the site will be accessible for users living in Rexburg Idaho, however 
                    the rest of the site is for English speakers looking to take steps to a healthier lifestyle. Target audience is 
                    motivated by uplifting influences and success stories that make working out look like a positive and fun 
                    experience. Site will be accessible by all browsers and while it will be aimed towards a younger 
                    generation that is social media oriented, it will also be easy to navigate for older generations. 
                </p>
